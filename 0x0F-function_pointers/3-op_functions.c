#include "3-calc.h"

/**
 * op_add - A function that performs addition operation
 * @a: First Integer
 * @b: Second Integer
 *
 * Return: (a + b)
 */

int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub - A function that performs subtraction operation
 * @a: First Integer
 * @b: Second Integer
 *
 * Return: (a - b)
 */

int op_sub(int a, int b)
{
	return (a - b);
}

/**
 * op_mul - A function that performs multiplication operation
 * @a: First Integer
 * @b: Second Integer
 *
 * Return: (a * b)
 */

int op_mul(int a, int b)
{
	return (a * b);
}

/**
 * op_div - A function that performs division operation
 * @a: First Integer
 * @b: Second Integer
 *
 * Return: (a / b)
 */

int op_div(int a, int b)
{
	return (a / b);
}

/**
 * op_mod - A function that performs modulus operation
 * @a: First Integer
 * @b: Second Integer
 *
 * Return: (a % b)
 */

int op_mod(int a, int b)
{
	return (a % b);
}
